<h1>Accessibility</h1>

<blockquote>
	„If our mission is to be customer-driven, connected, simple, helpful and inclusive – accessibility
	must be part of that!“
</blockquote>

<h2>Barrierefreiheitsrichtlinien v2</h2>

<ul>
	<li>
		<strong>Accessibility soll in jeder Phase der Arbeit mitgedacht werden</strong>, sei es UX/UI,
		Entwicklung, Produktmanagement oder Bildredaktion. Das gilt für alle öffentlich sichtbaren
		Features und bedeutet, dass wir bereits in der Konzeption Barrieren und mögliche Auswirkungen
		mitdenken. Wir wollen nicht nur im Nachhinein Probleme beheben, sondern proaktiv Lösungen
		entwickeln.
	</li>

	<li>
		<strong>Accessibility ist im Design-System dokumentiert</strong>. Diese Dokumentation
		unterstützt die Umsetzung von Features und erleichtert den Übergang an die Entwicklung. Aspekte
		wie Fokus-Reihenfolge, semantische Auszeichnung oder zugängliche Namen (Accessible Names) werden
		so direkt mitgedacht.
	</li>

	<li>
		<strong>Accessibility und Inklusion gehen Hand in Hand</strong>. Barrierefreiheit ist ein
		wichtiger Teil der Inklusion, aber nicht der einzige. Unsere Produkte sollen möglichst viele
		unterschiedliche Menschen ansprechen und einbeziehen. Dazu gehören z. B.:
		<ul>
			<li>Einfache Sprache für Menschen mit Lernschwierigkeiten oder geringer Sprachkompetenz</li>
			<li>Kulturelle Sensibilität bei Bildsprache, Farbwahl oder Symbolik</li>
			<li>
				Gendergerechte Sprache und diverse Darstellungen, die sich an unterschiedliche Identitäten
				richten
			</li>
			<li>
				Personalisierbare Einstellungen, wie z.B. Schriftgrößen, Kontraste oder reduzierte
				Animationen
			</li>
			<li>
				Interaktive Inhalte, die unterschiedliche Lern- oder Nutzungstypen berücksichtigen (visuell,
				auditiv, taktil)
			</li>
		</ul>
	</li>

	<li>
		<strong>Der Ist-Stand unserer Produkte wird regelmäßig auditiert</strong>. Wir testen unsere
		Features kontinuierlich, auch nach dem Rollout, um ungewollte Barrieren zu identifizieren. Diese
		Audits werden dokumentiert und als Grundlage für Verbesserungen genutzt.
	</li>
	<li>
		<strong>Regelmäßige Weiterbildung wird aktiv gefördert</strong>. Alle Teams sollen Zugang zu
		aktuellen Informationen und Schulungen im Bereich Accessibility erhalten – intern wie extern. So
		stellen wir sicher, dass unser Wissen immer auf dem neuesten Stand bleibt.
	</li>

	<li>
		<strong>Unser Ziel ist es, so barrierearm wie möglich zu sein.</strong> Wir verstehen, dass vollständige
		Barrierefreiheit ein Ideal ist. Unser Anspruch ist, jeden Tag besser zu werden – barriereärmer als
		gestern. Entscheidungen zur Umsetzung orientieren sich bewusst an den jeweiligen Use Cases – auch
		unter Berücksichtigung von Ressourcen.
	</li>

	<li>
		<strong>Wir teilen unser Wissen</strong>. Erkenntnisse, Best Practices und Lessons Learned
		werden im Unternehmen sichtbar gemacht, um Accessibility aktiv zu fördern und als
		selbstverständlichen Bestandteil der Produktentwicklung zu etablieren.
	</li>

	<li>
		<strong>Expertise von außen wird eingebunden</strong>. Wir möchten mit
		Accessibility-Expert:innen, Menschen mit Behinderungen und anderen Betroffenen zusammenarbeiten.
		Ihre Perspektiven sind essenziell, um echte Barrierefreiheit zu ermöglichen.
	</li>
</ul>

<h2>Barrierefreiheitsrichtlinien</h2>

<ul>
	<li>Wir wollen Accessibility als Bestandteil in jeder Arbeitsphase verstehen.</li>
	<li>Wir wollen Accessibility als Bestandteil im DS dokumentieren.</li>
	<li>Wir wollen Accessibility als inklusives Thema behandeln.</li>
	<li>Wir wollen unseren Ist-Stand regelmäßig auditieren.</li>
	<li>
		Wir wollen (unsere Technik) schon frühzeitig das Ergebnis unserer Arbeit unterstützend, nicht
		ausgrenzend, ausrichten.
	</li>
	<li>
		Wir wollen anstreben, uns regelmäßig fortzubilden, um den neusten Kenntnisstand für
		Accessibility-Themen sicherzustellen.
	</li>
	<li>Wir nehmen uns vor, so a wie möglich zu sein, um bestmögliche Qualität zu erzeugen.</li>
	<li>
		Wir wollen Entscheidungen zu Umfang von Accessibility bewusst am use case ausrichten und dabei
		kostenbewusst denken.
	</li>
	<li>
		Wir nehmen uns vor, das Verständnis im ganzen Haus zu schärfen und das Thema Accessibility
		präsent zu halten.
	</li>
	<li>Wir wollen nicht nur rückwirkend glattziehen, sondern auch vorausschauend agieren.</li>
	<li>
		Wir möchten uns für die Sicherstellung bestmöglicher Accessibility Expertinnen und Betroffene an
		die Seite holen.
	</li>
	<li>
		Wir nehmen uns vor, Erkenntnisse regelmäßig zu teilen, um die Zugänglichkeit für Accessibility
		zu fördern.
	</li>
</ul>
